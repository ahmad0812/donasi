                          <div class="entry-content">
                            <p align="justify">
                                Bulan Ramadhan telah tiba. Inilah bulan penuh berkah. Setiap muslim yang mendambakan rahmat dari Rabb-nya, merindukan maghfirah-Nya, dan berharap keberkahan dari-Nya, pasti akan bergembira menyambut tamu agung ini. Inilah bulan dimana kita dapat mengumpulkan sebanyak mungkin pahala dari Allah melalui berbagai amal shaleh yang telah Allah perintahkan.
                            </p>
                            <p align="justify">
                                Jika kita cermat dan bersungguh-sungguh mencari pahala Allah, maka Ramadhan adalah momentum yang paling tepat untuk melakukannya. Karena dibulan ini, kita tidak hanya dapat meraih pahala dari amalan kita sendiri, tapi kita juga bisa melipatgandakannya dengan cara beramal untuk orang lain, sebagaimana sabda Rasulullah shallallahu alaihi wa sallam:
                            </p>
                            <h3 style="direction: rtl;">
                            مَنْ فَطَرَ صَائِمًا كَانَ لَهُ مِثْلُ أَجْرِهِ غَيْرَ أَنَّهُ لَا يَنْقُصُ مِنْ أَجْرِ الصَائِمِ شَيْءٌ (رواه الترمذي وقال حديث حسن صحيح)
                            </h3>
                            <blockquote align="justify">
                            “Barangsiapa yang memberi makanan berbuka bagi orang berpuasa, maka baginya pahala yang semisal orang yang berpuasa tersebut tanpa mengurangi pahala orang yang berpuasa tersebut sedikitpun.” (HR. At Tirmidzi, beliau berkata, “Hadits Hasan Shahih”)
                            </blockquote>
                            <p align="justify">
                                Rasulullah shallallahu alaihi wa sallam tidak hanya menganjurkan dengan lisannya. Beliaupun telah melakukannya jauh sebelum umatnya. Bahkan, dalam berbagai riwayat disebutkan bahwa diantara kebiasaan yang beliau lakukan di bulan Ramadhan adalah memperbanyak sedekah. Beliau sangat terkenal kedermawanannya, dan sifat tersebut makin terlihat di bulan suci Ramadhan. Siapapun yang meminta sesuatu kepada beliau, Rasulullah tidak pernah menolaknya.
                                Selain itu, Rasulullah menyampaikan kabar gembira bagi orang-orang yang bersedekah dengan diampuni dosa-dosanya. Sebagaimana sabda Rasulullah shallallahu alaihi wa sallam:
                            </p>
                            <h3 style="direction: rtl;">
                            وَالصَدَقَةُ تُطْفِئُ الخَطِيْئَةَ كَمَا تُطْفِئُ المَاءَ النَّارَ (رواه الترمذي)
                            </h3>
                            <blockquote align="justify">
                            “Sedekah dapat menghapus dosa sebagaimana air memadamkan api” (HR. At Tarmidzi).
                            </blockquote>
                            Dan Rasulullah shallallahu alaihi wa sallam bersabda :
                            <h3 style="direction: rtl;">
                            اِنَّمَا الدّنيا لأربعة نفر: عبد رزقه الله مالاً وعلمًا فهو يتقي فيه ربّه ويصل فيه رحمه، ويعلم لله فيه حقا فهذا بأفضل المنازل. (رواه الترمذي وقال حديث حسن صحيح)
                            </h3>
                           
                            <blockquote align="justify">
                                “Dunia itu untuk 4 jenis hamba: Hamba yang diberikan rizqi oleh Allah serta kepahaman ilmu agama. Ia bertaqwa kepada Allah dalam menggunakan hartanya dan ia gunakan untuk menyambung silaturrahim. Dan ia menyadari terdapat hak Allah pada hartanya. Maka inilah kedudukan hamba yang paling baik.” (HR. At Tirmidzi, beliau berkata, “Hadits Hasan Shahih”)
                            </blockquote>
                            <p align="justify">
                                Atas dasar hal itu, kami segenap alumni Pondok Pesantren Modern Al-Mizan angkatan 2011 bermaksud menyelenggarakan kegiatan Santunan Anak Yatim dan Berbuka Puasa Bersama. Semoga acara ini dapat menjadi wasilah bagi kita untuk menghidupkan sunnah Rasulullah shallallahu ‘alaihi wa sallam di bulan Ramadhan. Selain itu kegiatan ini diharapkan memberi kesempatan kepada para dermawan/dermawati, pribadi maupun lembaga, untuk bersilaturrahmi dan ikut serta dalam mengamalkan sunnah Rasulullah Shalallahu ‘alaihi wasallam yaitu berbagi ni’mat bersama orang-orang berpuasa.
                            </p>
